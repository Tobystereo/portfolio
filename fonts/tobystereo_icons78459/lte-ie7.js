window.onload = function() {
	function addIcon(el, entity) {
		var html = el.innerHTML;
		el.innerHTML = '<span style="font-family: \'tobystereo_icons\'">' + entity + '</span>' + html;
	}
	var icons = {
			'icon-glasses' : '&#xe000;',
			'icon-glasses-2' : '&#x67;&#x6c;&#x61;&#x73;&#x73;&#x65;&#x73;',
			'icon-glasses-3' : '&#x67;'
		},
		els = document.getElementsByTagName('*'),
		i, attr, html, c, el;
	for (i = 0; ; i += 1) {
		el = els[i];
		if(!el) {
			break;
		}
		attr = el.getAttribute('data-icon');
		if (attr) {
			addIcon(el, attr);
		}
		c = el.className;
		c = c.match(/icon-[^\s'"]+/);
		if (c && icons[c[0]]) {
			addIcon(el, icons[c[0]]);
		}
	}
};